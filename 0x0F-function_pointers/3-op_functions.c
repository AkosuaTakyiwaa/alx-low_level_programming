#include "3-calc.h"

/**
 * op_add - fxn checks the sum of two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function checkss difference of two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function checkss product of two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fxn checks division of two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: division of a and b result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fxn checks remainder of the division of two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: remainder of a divided b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
